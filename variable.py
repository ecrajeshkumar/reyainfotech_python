# variable and Multiple assignment
# Variable is a name that refers to a value. It is used to store data in memory
x = 5
y = 10
z = 15  
print(x, y, z)  # Output: 5 10 15
sentence = "Hello, World!"
print(sentence)  # Output: Hello, World!

search, bob, mike = "search", "bob", "mike"
print(search)  # Output: search
print(bob)     # Output: bob
print(mike)    # Output: mike

search = bob = mike = "search"
print(search)  # Output: search
print(bob)     # Output: search
print(mike)    # Output: search

name, age, city = "Alice", 30, "New York"
print(name)  # Output: Alice
print(age)   # Output: 30
print(city)  # Output: New York

